/*
	在一个程序执行的过程中，各条语句的执行顺序对程序的结果是有直接影响的。
	也就是说程序的流程对运行结果有直接的影响。所以，我们必须清楚每条语句的执行流程。
	而且，很多时候我们要通过控制语句的执行顺序来实现我们要完成的功能。
	
	流程控制语句分类:
					顺序结构
					选择结构
					循环结构
	顺序结构概述:
				是程序中最简单最基本的流程控制，没有特定的语法结构，按照代码的先后顺序，依次执行，程序中大多数的代码都是这样执行的。
				总的来说：写在前面的先执行，写在后面的后执行
*/
